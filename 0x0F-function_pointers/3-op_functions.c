#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first int
 * @b: second int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns sub
 * @a: fi
 * @b: si
 *
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns mul
 * @a: fi
 * @b: si
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns div
 * @a: fi
 * @b: si
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns mod
 * @a: fi
 * @b: si
 *
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
